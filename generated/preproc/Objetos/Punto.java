package Objetos;

public class Punto 
{
	float x,y;

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public Punto(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
}
